def read_shell_config(config_path: str) -> dict[str, str]:
    resolved = Path(config_path).expanduser().resolve()
    if not resolved.is_file():
        raise FileNotFoundError(f"--config does not exist: {resolved}")

    script = "\n".join(
        [
            "set -euo pipefail",
            f"source {shlex.quote(str(resolved))}",
            *[
                f"printf '%s\\0%s\\0' {shlex.quote(name)} \"${{{name}:-}}\""
                for name in CONFIG_ENV_NAMES
            ],
        ]
    )
    completed = subprocess.run(
        ["bash", "-c", script],
        check=True,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
    )
    parts = [part.decode("utf-8") for part in completed.stdout.split(b"\0")]
    return {
        parts[index]: parts[index + 1]
        for index in range(0, len(parts) - 1, 2)
        if parts[index + 1]
    }


def get_megatron_arg_defaults(megatron_path: str) -> dict[str, Any]:
    arguments_path = (
        Path(megatron_path).expanduser().resolve() / "megatron" / "training" / "arguments.py"
    )
    if not arguments_path.is_file():
        raise FileNotFoundError(
            f"cannot read Megatron-LM argument defaults: {arguments_path}"
        )

    tree = ast.parse(arguments_path.read_text(encoding="utf-8"), filename=str(arguments_path))
    defaults: dict[str, Any] = {}
    for node in ast.walk(tree):
        if not isinstance(node, ast.Call):
            continue
        if not isinstance(node.func, ast.Attribute) or node.func.attr != "add_argument":
            continue

        option_names = [
            arg.value
            for arg in node.args
            if isinstance(arg, ast.Constant) and isinstance(arg.value, str)
        ]
        matched_options = [
            option_name
            for option_name in option_names
            if option_name in MEGATRON_DEFAULT_OPTIONS
        ]
        if not matched_options:
            continue

        keyword_by_name = {keyword.arg: keyword.value for keyword in node.keywords}
        if "default" in keyword_by_name:
            default_value = ast.literal_eval(keyword_by_name["default"])
        else:
            action_node = keyword_by_name.get("action")
            action = (
                action_node.value
                if isinstance(action_node, ast.Constant)
                and isinstance(action_node.value, str)
                else None
            )
            if action == "store_false":
                default_value = True
            elif action == "store_true":
                default_value = False
            else:
                default_value = None

        for option_name in matched_options:
            defaults[MEGATRON_DEFAULT_OPTIONS[option_name]] = default_value

    return defaults


def apply_config_and_defaults(
    args: argparse.Namespace,
    parser: argparse.ArgumentParser,
) -> None:
    config = read_shell_config(args.config) if args.config else {}
    env_to_attr = {
        "MEGATRON_PATH": ("megatron_path", str),
        "GLOBAL_BATCH_SIZE": ("global_batch_size", int),
        "TRAIN_STEPS": ("train_iters", int),
        "SEQ_LENGTH": ("seq_length", int),
        "SEED": ("seed", int),
        "SPLIT": ("split", str),
        "TOKENIZER_TYPE": ("tokenizer_type", str),
        "TOKENIZER_MODEL": ("tokenizer_model", str),
        "MAKE_VOCAB_SIZE_DIVISIBLE_BY": ("make_vocab_size_divisible_by", int),
        "VOCAB_EXTRA_IDS": ("vocab_extra_ids", int),
        "DATA_CACHE_PATH": ("data_cache_path", str),
    }
    for env_name, (attr_name, converter) in env_to_attr.items():
        if getattr(args, attr_name) is None and env_name in config:
            setattr(args, attr_name, converter(config[env_name]))
    if args.data_path is None and "DATA_PATH" in config:
        args.data_path = shlex.split(config["DATA_PATH"])

    megatron_defaults = {}
    if args.megatron_path is not None:
        megatron_defaults = get_megatron_arg_defaults(args.megatron_path)
    for attr_name, default_value in megatron_defaults.items():
        if default_value is not None and getattr(args, attr_name) is None:
            setattr(args, attr_name, default_value)

    if args.split is None and args.data_path is not None:
        # Match Megatron-LM's effective legacy default in validate_args().
        args.split = "969, 30, 1"

    for attr_name, default_value in SCRIPT_DEFAULTS.items():
        if getattr(args, attr_name) is None:
            setattr(args, attr_name, default_value)

    missing = []
    for attr_name, option_name in (
        ("megatron_path", "--megatron-path"),
        ("global_batch_size", "--global-batch-size"),
        ("train_iters", "--train-iters"),
        ("seq_length", "--seq-length"),
        ("seed", "--seed"),
        ("split", "--split"),
        ("tokenizer_type", "--tokenizer-type"),
        ("tokenizer_model", "--tokenizer-model"),
        ("data_path", "--data-path"),
    ):
        if getattr(args, attr_name) in (None, []):
            missing.append(option_name)
    if missing:
        parser.error(
            "missing required arguments after applying --config: "
            + ", ".join(missing)
        )


def configure_megatron_imports(megatron_path: str) -> None:
    global torch
    global BlendedMegatronDatasetBuilder
    global build_tokenizer
    global GPTDataset
    global GPTDatasetConfig

    resolved = Path(megatron_path).expanduser().resolve()
    if not resolved.is_dir():
        raise FileNotFoundError(f"--megatron-path does not exist: {resolved}")
    if not (resolved / "megatron").is_dir():
        raise FileNotFoundError(
            f"--megatron-path must point to a Megatron-LM repository: {resolved}"
        )

    resolved_str = str(resolved)
    if resolved_str not in sys.path:
        sys.path.insert(0, resolved_str)

    try:
        import torch as imported_torch
        from megatron.core.datasets.blended_megatron_dataset_builder import (
            BlendedMegatronDatasetBuilder as ImportedBlendedMegatronDatasetBuilder,
        )
        from megatron.core.datasets.gpt_dataset import (
            GPTDataset as ImportedGPTDataset,
            GPTDatasetConfig as ImportedGPTDatasetConfig,
        )
        from megatron.training.tokenizer import build_tokenizer as imported_build_tokenizer
    except ImportError as exc:
        raise ImportError(
            "Failed to import Megatron-LM modules. Make sure the Python virtual "
            "environment for Megatron-LM is activated before running this script."
        ) from exc

    torch = imported_torch
    BlendedMegatronDatasetBuilder = ImportedBlendedMegatronDatasetBuilder
    build_tokenizer = imported_build_tokenizer
    GPTDataset = ImportedGPTDataset
    GPTDatasetConfig = ImportedGPTDatasetConfig


@contextmanager
def rank_zero_when_distributed_is_not_initialized():
    if torch.distributed.is_initialized():
        yield
        return

    original_get_rank = torch.distributed.get_rank
    torch.distributed.get_rank = lambda *args, **kwargs: 0
    try:
        yield
    finally:
        torch.distributed.get_rank = original_get_rank


